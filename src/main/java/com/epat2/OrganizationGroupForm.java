package com.epat2;

public class OrganizationGroupForm {

	private Long orgGroupDetailId = null;
	private Long orgDetailId = null;
	private String orgGroupName = null;

	public Long getOrgDetailId() {
		return orgDetailId;
	}

	public void setOrgDetailId(Long orgDetailId) {
		this.orgDetailId = orgDetailId;
	}

	public String getOrgGroupName() {
		return orgGroupName;
	}

	public void setOrgGroupName(String orgGroupName) {
		this.orgGroupName = orgGroupName;
	}

	public Long getOrgGroupDetailId() {
		return orgGroupDetailId;
	}

	public void setOrgGroupDetailId(Long orgGroupDetailId) {
		this.orgGroupDetailId = orgGroupDetailId;
	}

}
